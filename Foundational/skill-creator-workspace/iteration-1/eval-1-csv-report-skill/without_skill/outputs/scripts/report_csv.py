def auto_chart_type(group_cols, df_pivot):
    """Heuristic chart type selection."""
    if len(df_pivot) > 10:
        return "barh"  # horizontal bar for many categories
    if len(df_pivot) <= 6:
        return "pie"
    return "bar"


def make_chart(df_pivot, metrics, chart_type, title, output_path):
    """Generate and save chart."""
    fig, ax = plt.subplots(figsize=(10, 6))
    
    if chart_type == "pie":
        metric = metrics[0] if len(metrics) == 1 else metrics[0]
        df_pivot[metric].plot(kind="pie", ax=ax, autopct="%1.1f%%", startangle=90)
        ax.set_ylabel("")
    elif chart_type == "line":
        df_pivot[metrics].plot(kind="line", ax=ax, marker="o")
    elif chart_type == "barh":
        df_pivot[metrics].plot(kind="barh", ax=ax)
    elif chart_type == "heatmap":
        # Fallback to bar for simple heatmap request
        df_pivot[metrics].plot(kind="bar", ax=ax)
    else:
        df_pivot[metrics].plot(kind="bar", ax=ax)
    
    ax.set_title(title, fontsize=14, fontweight="bold", pad=15)
    ax.grid(axis="y", alpha=0.3)
    plt.tight_layout()
    fig.savefig(output_path, dpi=150, bbox_inches="tight")
    plt.close(fig)


def format_number(n):
    """Human-friendly number formatting."""
    if abs(n) >= 1_000_000:
        return f"{n/1_000_000:.1f}M"
    if abs(n) >= 1_000:
        return f"{n/1_000:.1f}K"
    if isinstance(n, float):
        return f"{n:.2f}"
    return str(n)


def main():
    parser = argparse.ArgumentParser(description="Generate CSV report")
    parser.add_argument("--file", required=True)
    parser.add_argument("--group-by", nargs="+", required=True)
    parser.add_argument("--metrics", nargs="+", required=True)
    parser.add_argument("--agg", default="sum", choices=["sum", "mean", "count", "median", "min", "max"])
    parser.add_argument("--chart-type", default=None, choices=["bar", "barh", "line", "pie", "heatmap"])
    parser.add_argument("--title", default=None)
    parser.add_argument("--output", default="./csv-report-output")
    parser.add_argument("--top-n", type=int, default=None)
    parser.add_argument("--sort-by", default=None)
    parser.add_argument("--encoding", default="utf-8")
    args = parser.parse_args()

    os.makedirs(args.output, exist_ok=True)

    # Read data
    try:
        df = pd.read_csv(args.file, encoding=args.encoding)
    except Exception as e:
        print(json.dumps({"error": f"Failed to read CSV: {e}"}))
        sys.exit(1)

    # Validate columns
    for col in args.group_by + args.metrics:
        if col not in df.columns:
            print(json.dumps({"error": f"Column '{col}' not found. Available: {list(df.columns)}"}))
            sys.exit(1)

    # Build pivot table
    pivot = df.groupby(args.group_by, dropna=False)[args.metrics].agg(args.agg)
    
    # Sort
    sort_col = args.sort_by or args.metrics[0]
    if sort_col in pivot.columns:
        pivot = pivot.sort_values(sort_col, ascending=False)
    
    # Top N
    if args.top_n:
        pivot = pivot.head(args.top_n)

    # Chart type
    chart_type = args.chart_type or auto_chart_type(args.group_by, pivot)
    
    # Title
    title = args.title or f"{', '.join(args.metrics)} by {', '.join(args.group_by)}"
    
    # Generate chart
    chart_path = os.path.join(args.output, "chart.png")
    make_chart(pivot, args.metrics, chart_type, title, chart_path)

    # Key stats
    key_stats = []
    for metric in args.metrics:
        total = df[metric].sum()
        key_stats.append({"label": f"Total {metric}", "value": format_number(total)})
        key_stats.append({"label": f"Avg {metric}", "value": format_number(df[metric].mean())})

    # Render HTML report
    template = Template(REPORT_TEMPLATE)
    html = template.render(
        title=title,
        filename=os.path.basename(args.file),
        row_count=len(df),
        col_count=len(df.columns),
        key_stats=key_stats,
        pivot_html=pivot.to_html(classes="pivot-table", float_format=lambda x: f"{x:,.2f}"),
    )
    
    report_path = os.path.join(args.output, "report.html")
    with open(report_path, "w") as f:
        f.write(html)

    # Save pivot CSV
    pivot_csv_path = os.path.join(args.output, "pivot.csv")
    pivot.to_csv(pivot_csv_path)

    # Output summary
    result = {
        "status": "success",
        "report_html": report_path,
        "chart_png": chart_path,
        "pivot_csv": pivot_csv_path,
        "pivot_shape": list(pivot.shape),
        "title": title,
    }
    print(json.dumps(result, indent=2))


if __name__ == "__main__":
    main()
