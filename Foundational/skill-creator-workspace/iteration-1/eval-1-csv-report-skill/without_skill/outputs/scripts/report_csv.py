#!/usr/bin/env python3
"""
report_csv.py — Generate a full HTML report with pivot table and chart from a CSV.

Usage:
    python3 report_csv.py --file data.csv --group-by region --metrics revenue --agg sum \
        --chart-type bar --title "Sales by Region" --output ./output
"""

import argparse
import json
import os
import sys

import matplotlib
matplotlib.use("Agg")  # Headless rendering
import matplotlib.pyplot as plt
import pandas as pd
from jinja2 import Template


